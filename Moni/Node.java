package Moni;

public class Node {
	char direct;
	boolean visited;
	
	public void setN() {
		direct = 'N';
		visited = false;
	}
	
	public void setS() {
		direct = 'S';
		visited = false;
	}
	public void setW() {
		direct = 'W';
		visited = false;
	}
	public void setE() {
		direct = 'N';
		visited = false;
	}

}
